vim.opt.relativenumber = true -- Relative line numbers

vim.opt.ignorecase = true  -- Case-insensitive search
vim.opt.smartcase = true  -- Case-insensitive search
vim.opt.incsearch = true   -- Shows matches while typing

vim.opt.termguicolors = true -- Enables 24-bit colors
vim.opt.scrolloff = 8     -- Keeps 4 lines above/below cursor
vim.opt.signcolumn = "yes" -- Always show sign column (for diagnostics)

